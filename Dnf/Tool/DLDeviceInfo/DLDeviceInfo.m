+(uint64_t)getMachAbsoluteTime
{
    mach_timebase_info_data_t info;
    mach_timebase_info(&info);
    uint64_t time = mach_absolute_time();
    return time * info.numer / info.denom;
}

+ (NSString *)GUID{
    
    CFUUIDRef uuid_ref = CFUUIDCreate(NULL);
    CFStringRef uuid_string_ref = CFUUIDCreateString(NULL, uuid_ref);
    CFRelease(uuid_ref);
    NSString *uuid = [NSString stringWithString:(__bridge NSString *)uuid_string_ref];
    CFRelease(uuid_string_ref);
    
    return uuid;
}

+ (NSString*)UDID
{
    NSString *udid = [DLKeyChain getValueWithKey:MS_UUID_KEY];
    if (!udid) {
        
        NSString *sysVersion = [UIDevice currentDevice].systemVersion;
        CGFloat version = [sysVersion floatValue];
        
        if (version >= 6.0) {
            udid = [DLDeviceInfo _UDID_iOS7];
        }
        else{
            udid = [DLDeviceInfo _UDID_iOS6];
        }
        
        [DLKeyChain addValue:udid withKey:MS_UUID_KEY];
    }
    
    return udid;
  
}

